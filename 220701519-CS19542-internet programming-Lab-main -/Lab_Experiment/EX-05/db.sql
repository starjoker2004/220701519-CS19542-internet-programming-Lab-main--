CREATE DATABASE LibraryDB;
USE LibraryDB;

CREATE TABLE BOOK (
    ACCNO INT AUTO_INCREMENT PRIMARY KEY,
    TITLE VARCHAR(100),
    AUTHOR VARCHAR(100),
    PUBLISHER VARCHAR(100),
    EDITION VARCHAR(50),
    PRICE DECIMAL(10, 2)
);

CREATE TABLE MEMBER (
    MID INT AUTO_INCREMENT PRIMARY KEY,
    MNAME VARCHAR(100),
    BRANCH VARCHAR(50)
);

CREATE TABLE FINE (
    MID INT,
    FINE_DATE DATE,
    AMOUNT DECIMAL(10, 2),
    FOREIGN KEY (MID) REFERENCES MEMBER(MID)
);
